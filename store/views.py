from django.contrib.auth import authenticate, login, logout
from django.db.models import ProtectedError
from django.http import Http404
from rest_framework import status, permissions
from rest_framework.response import Response
from store.models import Book, Author, Publisher, User, Cart, Order
from rest_framework.views import APIView
from store.serializers import BookSerializer, PublisherSerializer, UserSerializer, CartSerializer, OrderSerializer, \
    AuthorSerializer, UserRegistrationSerializer
import logging

logger = logging.getLogger(__name__)


class UserRegistrationView(APIView):
    permission_classes = [permissions.AllowAny]

    def post(self, request):
        serializer = UserRegistrationSerializer(data=request.data)
        if serializer.is_valid():
            user = serializer.save()
            logger.info(f"User {user.username} registered successfully")
            return Response(
                {"message": "Your user is now registered"},
                status=status.HTTP_201_CREATED
            )
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


class LoginView(APIView):
    permission_classes = [permissions.AllowAny]

    def post(self, request):
        username = request.data.get('username')
        password = request.data.get('password')

        user = authenticate(username=username, password=password)

        if user:
            login(request, user)
            logger.info(f"User {username} logged in successfully")
            return Response({
                "message": "Successfully logged in",
                "user": {
                    "id": user.id,
                    "username": user.username,
                    "email": user.email
                }
            })

        logger.warning(f"Login failed for username: {username}")
        return Response(
            {"error": "Incorrect username or password"},
            status=status.HTTP_401_UNAUTHORIZED
        )


class LogoutView(APIView):
    def post(self, request):
        logger.info(f"User {request.user.username} logged out")
        logout(request)
        return Response({"message": "Successfully logged out"})


